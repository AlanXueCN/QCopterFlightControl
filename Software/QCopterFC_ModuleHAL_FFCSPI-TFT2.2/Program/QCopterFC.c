#include "drivers\stm32f4_system.h"
#include "modules\module_ili9341.h"

#include "QCopterFC.h"
/*====================================================================================================*/
/*====================================================================================================*/
void QCopterFC_Init( void )
{
  HAL_Init();

  GPIO_Config();
  ILI9341_Config();

  ILI9341_Init();

  Delay_100ms(1);
}

int main( void )
{
  QCopterFC_Init();

  LCD_TestColoBar();

  while(1) {
    LED_R_Reset();
    LED_G_Reset();
    LED_B_Reset();
    Delay_10ms(2);
    LED_R_Set();
    LED_G_Set();
    LED_B_Set();
    Delay_10ms(98);
  }
}
