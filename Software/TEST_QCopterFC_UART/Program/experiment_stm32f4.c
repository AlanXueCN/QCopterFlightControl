#include "stm32f4_system.h"
#include "stm32f4_usart.h"
#include "QCopterFC_board.h"
#include "module_rs232.h"
#include "algorithm_string.h"
/*=====================================================================================================*/
/*=====================================================================================================*/
int main( void )
{
  u8 i = 0;

  SystemInit();
  LED_Config();
  KEY_Config();
  RS232_Config();

  while(1) {
    LED_G = !LED_G;

    i++;

    RS232_SendStr((u8*)"i = ");
    RS232_SendNum(Type_D, 3, i);
    RS232_SendStr((u8*)"\r\n");

    if(i==255)  i = 0;

    Delay_100ms(1);
  }
}
