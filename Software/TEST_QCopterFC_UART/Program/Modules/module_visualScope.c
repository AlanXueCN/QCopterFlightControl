#include "stm32f4_system.h"
#include "module_rs232.h"
#include "module_visualScope.h"
#include "algorithm_crc.h"
/*====================================================================================================*/
/*====================================================================================================*
**函數 : VisualScope
**功能 : VisualScope
**輸入 : *SendBuf
**輸出 : None
**使用 : VisualScope(SendBuf);
**====================================================================================================*/
/*====================================================================================================*/
void VisualScope( u8 *SendBuf )
{
  u8 SendCRC[2] = {0};
  u16 UART_CRC = CRC_Check16(SendBuf, 8);

  SendCRC[0] = (UART_CRC&0x00ff);
  SendCRC[1] = (UART_CRC&0xff00)>>8;

  RS232_SendData(SendBuf, 8);
  RS232_SendData(SendCRC, 2);
}
