#include "drivers\stm32f4_system.h"

#include "QCopterFC.h"
/*====================================================================================================*/
/*====================================================================================================*/
void QCopterFC_Init( void )
{
  HAL_Init();
  GPIO_Config();
}

int main( void )
{
  QCopterFC_Init();

  while(1) {
    LED_R_Reset();
    LED_G_Reset();
    LED_B_Reset();
    Delay_10ms(2);
    LED_R_Set();
    LED_G_Set();
    LED_B_Set();
    Delay_10ms(98);

    while(KEY_Read()) {
      LED_R_Reset();
      Delay_100ms(5);
      LED_R_Set();
      LED_G_Reset();
      Delay_100ms(5);
      LED_G_Set();
      LED_B_Reset();
      Delay_100ms(5);
      LED_B_Set();
    }
  }
}
