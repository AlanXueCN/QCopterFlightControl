/*====================================================================================================*
**函數 : Cmp_ArrU8
**功能 : 比較 u8 Arr1 與 u8 Arr2
**輸入 : Arr1, Arr2, DataLen
**輸出 : SUCCESS/ERROR
**使用 : Cmp_ArrU8(Arr1, Arr2, sizeof(Arr1));
**====================================================================================================*/
/*====================================================================================================*/
u8 Cmp_ArrU8( uc8* Arr1, uc8* Arr2, u32 DataLen )
{
  u32 ErrCount = 0;

  while(DataLen) {
    DataLen--;
    if(Arr1[DataLen] != Arr2[DataLen])
      ErrCount++;
  }

  return ((ErrCount==0) ? SUCCESS : ERROR);
}
