/*=====================================================================================================*
**函數 : BubbleSortU8
**功能 : 氣泡排序法 8Bit
**輸入 : ArrSize, ArrU8
**輸出 : None
**使用 : BubbleSortU8(16, ArrU8);
**=====================================================================================================*/
/*=====================================================================================================*/
void BubbleSortU8( u16 ArrSize, u8* ArrU8 )
{
	u8 Temp = 0;
	u16 i = 0, j = 0;

	for(i=0; i<ArrSize-1; i++) {
		for(j=i+1; j<ArrSize; j++) {
			if(ArrU8[i]>ArrU8[j]) {
				Temp = ArrU8[i];
				ArrU8[i] = ArrU8[j];
				ArrU8[j] = Temp;
			}
		}
	}
}
