/*=====================================================================================================*/
/*=====================================================================================================*/
#include "stm32f4_system.h"
#include "QCopterFC_board.h"
/*=====================================================================================================*/
/*=====================================================================================================*/
#define LED_DELAY 5
/*=====================================================================================================*/
/*=====================================================================================================*/
int main( void )
{
  SystemInit();
  LED_Config();
  KEY_Config();

  while(1) {
    LED_R = !LED_R;
    Delay_10ms(LED_DELAY);
    LED_R = !LED_R;
    Delay_10ms(LED_DELAY);
    LED_G = !LED_G;
    Delay_10ms(LED_DELAY);
    LED_G = !LED_G;
    Delay_10ms(LED_DELAY);
    LED_B = !LED_B;
    Delay_10ms(LED_DELAY);
    LED_B = !LED_B;
    Delay_10ms(LED_DELAY);

    while(KEY == KEY_ON) {
      LED_R = !LED_R;
      LED_G = !LED_G;
      LED_B = !LED_B;
      Delay_10ms(LED_DELAY);
    }
  }
}
