#include "stm32f4_system.h"
#include "QCopterFC_board.h"
/*=====================================================================================================*/
/*=====================================================================================================*/
int main( void )
{
  SystemInit();
  LED_Config();
  KEY_Config();

  while(1) {
    LED_R = 0;
    LED_G = 1;
    LED_B = 1;
    Delay_100ms(1);
    LED_R = 1;
    LED_G = 0;
    LED_B = 1;
    Delay_100ms(1);
    LED_R = 1;
    LED_G = 1;
    LED_B = 0;
    Delay_100ms(1);

    while(KEY) {
      LED_R = 1;
      LED_G = 1;
      LED_B = 1;
      Delay_100ms(2);
      LED_R = 0;
      LED_G = 0;
      LED_B = 0;
      Delay_100ms(2);
    }
  }
}
